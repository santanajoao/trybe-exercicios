function soma(a, b) {
  return a + b;
}

function subtrai(a, b) {
  return a - b;
}

function multiplica(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function restoDivisao(a, b) {
  return a % b;
}

const a = 21;
const b = 7;

console.log(soma(a, b));
console.log(subtrai(a, b));
console.log(multiplica(a, b));
console.log(divide(a, b));
